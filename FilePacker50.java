import java.io.*;
import java.util.*;

class program606
{
    public static void main(String A[]) throws Exception
    {
        String Header = null;

        byte key = 0x11;

        int iRet = 0;
        int i = 0, j = 0;

        byte Buffer[] = new byte[1024];

        byte bHeader[] = new byte[100];

        Scanner sobj = new Scanner(System.in);
        
        System.out.println("Enter the name of folder:");
        String FolderName = sobj.nextLine();

        System.out.println("Enter the name of packed file:");
        String PackName = sobj.nextLine();

        File fobj = new File(FolderName);        // Marvellous : folder
    
        if((fobj.exists()) && fobj.isDirectory())
        {
            File PackObj = new File(PackName);

            PackObj.createNewFile();

            FileOutputStream foobj = new FileOutputStream(PackObj);   //FileOutputStream : It is used for writing data into file

            FileInputStream fiobj = null;

            System.out.println("Folder is present");

            File fArr[] = fobj.listFiles();       // This is array of  File class as object

            System.out.println("Number of files in the folder are : "+fArr.length);

            for(i = 0; i<fArr.length; i++)
            {
                fiobj = new FileInputStream(fArr[i]);
            
                
                if(fArr[i].getName().endsWith(".txt"))
                {
                    // Header formation
                    Header = fArr[i].getName() + " " + fArr[i].length();

                    for(j = Header.length(); j<100; j++)
                    {
                        Header = Header + " ";
                    }

                    bHeader = Header.getBytes();

                    // Write header into packed file
                    foobj.write(bHeader,0,100);
                    
                    //Read the data from input files from marvellouss folder
                    while((iRet = fiobj.read(Buffer)) != -1)
                    {
                        // Encryption 
                        for(j = 0; j<iRet; j++)
                        {
                            Buffer[j] = (byte)(Buffer[j] ^ key);
                        }

                        // Write the files data into packed file
                        foobj.write(Buffer,0,iRet);
                    }

                }
             
                fiobj.close();
            }
            foobj.close();
        }
        else
        {
            System.out.println("There is no such folder");
        }
      
    }
}